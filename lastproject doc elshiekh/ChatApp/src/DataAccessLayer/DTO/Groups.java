package DataAccessLayer.DTO;

/**
 * it is a class that represents group  with a group id and group name and their setters and getters.
 * @author team 4
 */
public class Groups implements java.io.Serializable {
    private String id;
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
